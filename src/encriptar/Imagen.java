    public void desencriptaImagen() {
        int b[][] = new int[largo][f];
        int cont = 0;
        for (int i = 0; i < f; i++) {
            for (int j = 0; j < largo; j++) {
                b[j][i] = rgbs[cont];
                cont = cont + 1;  }    }
        matriz2 = new int[largo][f];
        for (int i = 1; i <= f; i++) {
            for (int j = 0; j < largo; j++) {
                matriz2[j][i - 1] = b[j][(i * p) % f];  }  }
        cont = 0;
        desenc = new int[ancho * alto];

        for (int i = 0; i < f; i++) {
            for (int j = 0; j < largo; j++) {
                desenc[cont] = matriz2[j][i];
                cont = cont + 1;   }  }}
